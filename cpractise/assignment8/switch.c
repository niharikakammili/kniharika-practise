#include<stdio.h>
int main()
{
	int week;
	printf("enter the week \n");
	scanf("%d",&week);
	switch(week)
	{
case1:
		printf("monday");
		break;
	
case2:
	
		printf("tuesday");
		break;
	
case3:
	
		printf("wednsday");
		break;
	
case4:
	
		printf("thursdaay");
		break;
	
case5:
	
		printf("friday");
		break;
	
case6:
	
		printf("saturday");
		break;
	
case7:
	
		printf("sunday");
		break;
		default:
		printf("enter the invalid week between 1-7");
	}
	return 0;
}


